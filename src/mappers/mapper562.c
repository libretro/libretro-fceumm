static void Sync(void) {
	setprg8r(0x10, 0x6000, 0);
	/* PRG memory can be writable */
	SetupCartPRGMapping(0, PRGptr[0], PRGsize[0], !(reg1M & 0x02));
	if (regtgd & 0x80) {
		setprg8(0x8000, prg[0]);
		setprg8(0xA000, prg[1]);
		setprg8(0xC000, prg[2]);
		setprg8(0xE000, prg[3]);
	} else if (!(reg2M & 0x01)) {
		setprg8(0x8000, (prg[0] & 0x0F) | ((reg2M >> 2) & 0x10));
		setprg8(0xA000, (prg[1] & 0x0F) | ((reg2M >> 2) & 0x10));
		setprg8(0xC000, (prg[2] & 0x0F));
		setprg8(0xE000, (prg[3] & 0x0F));
	} else {
		switch (reg1M >> 5) {
		case 0:
			setprg16(0x8000, latch & 0x07);
			setprg16(0xC000, 0x07);
			break;
		case 1:
			setprg16(0x8000, (latch >> 2) & 0x0F);
			setprg16(0xC000, 0x07);
			break;
		case 2:
			setprg16(0x8000, latch & 0x0F);
			setprg16(0xC000, 0x0F);
			break;
		case 3:
			setprg16(0x8000, 0x0F);
			setprg16(0xC000, latch & 0x0F);
			break;
		case 4:
			setprg32(0x8000, (latch >> 4) & 0x03);
			break;
		case 5:
			setprg32(0x8000, 0x03);
			break;
		case 6:
			setprg8(0x8000, latch & 0x0F);
			setprg8(0xA000, latch >> 4);
			setprg16(0xC000, 0x07);
			break;
		case 7:
			setprg8(0x8000, latch & 0x0E);
			setprg8(0xA000, (latch >> 4) | 0x01);
			setprg16(0xC000, 0x07);
			break;
		}
	}

	/* CHR RAN can be write-protected */
	SetupCartCHRMapping(0, CHRptr[0], CHRsize[0],  !(((reg1M & 0xE0) & 0x80) || lockCHR));

	if (regtgd & 0x40) {
		setchr1(0x0000, chr1K[0]);
		setchr1(0x0400, chr1K[1]);
		setchr1(0x0800, chr1K[2]);
		setchr1(0x0C00, chr1K[3]);
		setchr1(0x1000, chr1K[4]);
		setchr1(0x1400, chr1K[5]);
		setchr1(0x1800, chr1K[6]);
		setchr1(0x1C00, chr1K[7]);
	} else {
		setchr8(chr);
	}

	switch (reg1M & 0x11) {
	case 0x00: setmirror(MI_0); break;
	case 0x01: setmirror(MI_V); break;
	case 0x10: setmirror(MI_1); break;
	case 0x11: setmirror(MI_H); break;
	}
}

static DECLFR(M562ReadReg) {
	switch (A) {
	case 0x4400:
	case 0x4401:
	case 0x4402:
	case 0x4403:
	case 0x4404:
	case 0x4405:
	case 0x4406:
	case 0x4407:
		return chr1K[A & 0x07];
	case 0x4408:
	case 0x4409:
	case 0x440A:
	case 0x440B:
		return (prg[A & 0x03] << 2) | (latch & 0x03);
	case 0x440C:
		return (IRQCount_tgd >> 8);
	case 0x440D:
		return (IRQCount_tgd & 0xFF);
	case 0x4411:
		return regtgd;
	case 0x4415:
		return reg1M;
	case 0x4420:
		return chr1K[lastCHRBank];
	default:
		if (A & 0x800) {
			return tgd4800[A & 0x7FF];
		}
		return CartBR(A);
	}
}

static DECLFW(M562WriteReg) {
	switch (A) {
	case 0x4024:
		X6502_IRQEnd(FCEU_IQEXT);
		break;
	case 0x4025:
		X6502_IRQEnd(FCEU_IQEXT);
		IRQa_fds = V;
		if (IRQa_fds & 0x42) {
			IRQCount_fds = 0;
		}
		break;
	case 0x42FC:
	case 0x42FD:
	case 0x42FE:
	case 0x42FF:
		reg1M = (V & 0xF0) | (A & 0x03);
		lockCHR = !((reg1M & 0xE0) & 0x80);
		Sync();
		break;
	case 0x43FC:
	case 0x43FD:
	case 0x43FE:
	case 0x43FF:
		reg2M = (V & 0xF0) | (A & 0x03);
		chr = V & 0x03;
		Sync();
		break;
	case 0x4400:
	case 0x4401:
	case 0x4402:
	case 0x4403:
	case 0x4404:
	case 0x4405:
	case 0x4406:
	case 0x4407:
		chr1K[A & 7] = V;
		Sync();
		break;
	case 0x440C:
		X6502_IRQEnd(FCEU_IQEXT);
		if (!(V & 0x80)) {
			IRQCount_tgd = 0x8000;
		}
		count_target_tgd = (count_target_tgd & 0x00FF) | (V << 8);
		break;
	case 0x440D:
		X6502_IRQEnd(FCEU_IQEXT);
		count_target_tgd = (count_target_tgd & 0xFF00) | V;
		break;
	case 0x4411:
		regtgd = V;
		Sync();
		break;
	}
}

static DECLFW(M562Write) {
	if (reg1M & 0x02) {
		latch = V;
		switch (reg1M >> 5) {
		case 1:
		case 4:
		case 5:
			chr = latch & 0x03;
			break;
		case 3:
			chr = (latch >> 4) & 0x03;
			break;
		}
		prg[(A >> 13) & 0x03] = V >> 2;
		Sync();
	} else {
		CartBW(A, V);
	}
}

static DECLFW(M562CHRWrite) {
	if (((reg1M >> 5) >= 5) && (~(reg1M & 0x11) & 0x01)) {
		lockCHR = !!(reg1M & 0x10);
	}
	writePPU(A, V);
}

static void M562Reset(void) {
	IRQa_fds = 0;
	IRQCount_fds = 0;
	IRQCount_tgd = 0xFFFF;
	count_target_tgd = 0;
	Sync();
}

static void M562Power(void) {
	reg1M = (iNESCart.submapper << 5) | ((iNESCart.mirror == MI_V) ? 0x01 : 0x11) | 0x04 | 0x02;
	reg2M = 0x03;
	regtgd = 0x03;

	latch = 0;
	chr = 0;
	lockCHR = FALSE;

	prg[0] = 0x1C;
	prg[1] = 0x1D;
	prg[2] = 0x1E;
	prg[3] = 0x1F;
	chr1K[0] = 0;
	chr1K[1] = 1;
	chr1K[2] = 2;
	chr1K[3] = 3;
	chr1K[4] = 4;
	chr1K[5] = 5;
	chr1K[6] = 6;
	chr1K[7] = 7;
	IRQa_fds = 0;
	IRQCount_fds = 0;
	IRQCount_tgd = 0xFFFF;
	count_target_tgd = 0;

	writePPU = GetWriteHandler(0x2007);
	SetWriteHandler(0x2007, 0x2007, M562CHRWrite);

	SetReadHandler(0x4020, 0x4FFF, M562ReadReg);
	SetWriteHandler(0x4020, 0x4FFF, M562WriteReg);

	SetReadHandler(0x8000, 0xFFFF, CartBR);
	SetWriteHandler(0x8000, 0xFFFF, M562Write);

	SetReadHandler(0x6000, 0x7FFF, CartBR);
	SetWriteHandler(0x6000, 0x7FFF, CartBW);
	FCEU_CheatAddRAM(WRAMSIZE >> 10, 0x6000, WRAM);

	Sync();

	if (MISC_ROM_SIZE && (MISC_ROM_SIZE >= 4)) {
		uint16 trainerLoadAddr = 0x7000;
		uint16 trainerInitAddr = 0x7003;
		uint32 trainerSize = 512;
		uint8 *trainerData = MISC_ROM_PTR;
		int i;

		if (MISC_ROM_SIZE != 512) {
			trainerLoadAddr = (MISC_ROM_PTR[1] << 8) | MISC_ROM_PTR[0];
			trainerInitAddr = (MISC_ROM_PTR[3] << 8) | MISC_ROM_PTR[2];
			trainerSize = MISC_ROM_SIZE - 4;
			trainerData = MISC_ROM_PTR + 4;

			FCEU_printf(" load addr : %04x\n", trainerLoadAddr);
			FCEU_printf(" init addr : %04x\n", trainerInitAddr);
			FCEU_printf(" data size : %d\n", trainerSize);
		}

		for (i = 0; i < MISC_ROM_SIZE; i++) {
			(GetWriteHandler(trainerLoadAddr))(trainerLoadAddr + i, trainerData[i]);
		}

		if (trainerInitAddr) {
			/* JSR init */
			(GetWriteHandler(0x0700))(0x0700, 0x20);
			(GetWriteHandler(0x0701))(0x0701, trainerInitAddr & 0xFF);
			(GetWriteHandler(0x0702))(0x0702, trainerInitAddr >> 8);

			/* JMP ($FFFC) */
			(GetWriteHandler(0x0703))(0x0703, 0x6C);
			(GetWriteHandler(0x0704))(0x0704, 0xFC);
			(GetWriteHandler(0x0705))(0x0705, 0xFF);

			X6502_SetNewPC(0x700);
		}
	}
	(GetWriteHandler(0x4017))(0x4017, 0x40);
}

static void StateRestore(int version) {
	Sync();
}

static void M562CPUIRQHook(int a) {
	while (a--) {
		IRQCount_fds += 3;
		while ((IRQCount_fds >= 448) && (IRQa_fds & 0x80)) {
			IRQCount_fds -= 448;
			X6502_IRQBegin(FCEU_IQEXT);
		}
		if (count_target_tgd & 0x8000) {
			if (IRQCount_tgd == count_target_tgd && (IRQCount_tgd != 0xFFFF)) {
				X6502_IRQBegin(FCEU_IQEXT);
			} else {
				IRQCount_tgd++;
			}
		}
	}
}

static void M562PPUHook(uint32 A) {
	if ((lastAddr != A) && ((A & 0x3000) != 0x2000)) {
		lastCHRBank = (A >> 13) & 0x07;
	}
	lastAddr = A;
}

void Mapper562_Init(CartInfo *info) {
	uint32 wramsize = info->PRGRamSize + info->PRGRamSaveSize;

	info->Power = M562Power;
	info->Reset = M562Reset;
	MapIRQHook = M562CPUIRQHook;
	PPU_hook = M562PPUHook;
	GameStateRestore = StateRestore;
	AddExState(StateRegs, ~0, 0, NULL);

	WRAMSIZE = wramsize ? wramsize : 8192;
	if (WRAMSIZE) {
		WRAM = (uint8 *)FCEU_malloc(WRAMSIZE);
		SetupCartPRGMapping(0x10, WRAM, WRAMSIZE, TRUE);
		AddExState(WRAM, WRAMSIZE, 0, "WRAM");
	}

	if (CHR_ROM_SIZE) {
		/* Game Doctor only used CHR RAM, but some .nes files has CHR ROM instead. */
		/* Remap CHR as writable. */
		SetupCartCHRMapping(0, CHR_ROM_PTR, CHR_ROM_SIZE, TRUE);
		AddExState(CHR_ROM_PTR, CHR_ROM_SIZE, 0, "CHRM");
	}

	/* PRG memory can be writable, so add to states */
	AddExState(PRG_ROM_PTR, PRG_ROM_SIZE, 0, "PRGM");
}
